public class civStats extends Civ{
    public int cultureScore;
    public int simScore;
    public int warScore;
    public int navalScore;

    public civStats (int cS, int sS, int wS, int nS) {
        cultureScore = cS;
        simScore = sS;
        warScore = wS;
        navalScore = nS;
    }
}
